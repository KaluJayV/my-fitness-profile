CREATE TABLE public.exercise_categories (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name text NOT NULL
);

CREATE TABLE public.exercises (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_id integer REFERENCES public.exercise_categories(id) ON DELETE SET NULL,
    name text NOT NULL,
    primary_muscles text[],
    gif_url text
);

CREATE TABLE public.users (
    id uuid PRIMARY KEY DEFAULT auth.uid() REFERENCES auth.users(id) ON DELETE CASCADE,
    username text,
    email text,
    goal text,
    experience text,
    injuries text[],
    equipment text[],
    created_at timestamptz DEFAULT now()
);

CREATE TABLE public.programs (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id uuid REFERENCES auth.users(id) ON DELETE CASCADE,
    name text,
    days_per_week integer,
    generator_source text,
    created_at timestamptz DEFAULT now()
);

CREATE TABLE public.workouts (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    program_id uuid REFERENCES public.programs(id) ON DELETE CASCADE,
    workout_date date,
    json_plan jsonb,
    completed boolean DEFAULT false
);

CREATE TABLE public.workout_exercises (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    workout_id uuid REFERENCES public.workouts(id) ON DELETE CASCADE,
    exercise_id integer REFERENCES public.exercises(id) ON DELETE SET NULL,
    "position" integer
);

CREATE TABLE public.sets (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    workout_exercise_id uuid REFERENCES public.workout_exercises(id) ON DELETE CASCADE,
    reps integer,
    weight numeric(6,2),
    rir integer,
    performed_at timestamptz DEFAULT now()
);

CREATE TABLE public.ratings (
    user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    exercise_id integer NOT NULL REFERENCES public.exercises(id) ON DELETE CASCADE,
    pump_score integer,
    notes text,
    updated_at timestamptz DEFAULT now(),
    manual_1rm numeric,
    manual_1rm_updated_at timestamptz,
    PRIMARY KEY (user_id, exercise_id),
    CONSTRAINT ratings_pump_score_check CHECK (pump_score >= 1 AND pump_score <= 5)
);

CREATE TABLE public.conversation_sessions (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    conversation_data jsonb DEFAULT '[]'::jsonb,
    analytics_cache jsonb,
    insights text[] DEFAULT ARRAY[]::text[],
    created_at timestamptz NOT NULL DEFAULT now(),
    updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE public.performance_metrics (
    id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    session_id text,
    metric_type text NOT NULL,
    value numeric NOT NULL,
    metadata jsonb DEFAULT '{}'::jsonb,
    "timestamp" timestamptz NOT NULL,
    created_at timestamptz NOT NULL DEFAULT now()
);

