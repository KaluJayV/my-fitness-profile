-- Progress view
CREATE VIEW public.v_progress AS
SELECT s.id, s.workout_exercise_id, s.weight, s.reps, s.rir, s.performed_at,
       w.workout_date, we.exercise_id, e.name AS exercise_name, p.user_id,
       (s.weight * s.reps::numeric) AS volume,
       CASE WHEN s.reps > 0 AND s.weight > 0
            THEN s.weight * (1::numeric + ((s.reps + COALESCE(s.rir,0))::numeric / 30.0))
            ELSE NULL END AS estimated_1rm
FROM public.sets s
JOIN public.workout_exercises we ON s.workout_exercise_id = we.id
JOIN public.workouts w ON we.workout_id = w.id
JOIN public.programs p ON w.program_id = p.id
JOIN public.exercises e ON we.exercise_id = e.id
WHERE w.completed = true;

-- Core lifts view
CREATE VIEW public.v_core_lifts AS
SELECT id, name,
  CASE
    WHEN lower(name) LIKE '%squat%' AND lower(name) NOT LIKE '%front%' THEN 'squat'
    WHEN lower(name) LIKE '%bench%' AND lower(name) NOT LIKE '%incline%' THEN 'bench'
    WHEN lower(name) LIKE '%deadlift%' THEN 'deadlift'
    WHEN lower(name) LIKE '%overhead press%' OR lower(name) LIKE '%military press%'
      OR (lower(name) LIKE '%press%' AND lower(name) LIKE '%standing%') THEN 'overhead_press'
    ELSE NULL
  END AS core_lift_type
FROM public.exercises
WHERE lower(name) LIKE '%squat%' OR lower(name) LIKE '%bench%' OR lower(name) LIKE '%deadlift%'
   OR lower(name) LIKE '%overhead press%' OR lower(name) LIKE '%military press%'
   OR (lower(name) LIKE '%press%' AND lower(name) LIKE '%standing%');

