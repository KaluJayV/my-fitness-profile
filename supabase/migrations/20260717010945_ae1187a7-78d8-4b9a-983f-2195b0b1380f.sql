CREATE OR REPLACE FUNCTION public.get_user_history(p_user uuid)
RETURNS TABLE(exercise_name text, total_sets bigint, avg_weight numeric, avg_reps numeric, last_performed timestamptz)
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public, pg_temp AS $$
  SELECT e.name, COUNT(s.id), AVG(s.weight), AVG(s.reps), MAX(s.performed_at)
  FROM public.exercises e
  JOIN public.workout_exercises we ON e.id = we.exercise_id
  JOIN public.sets s ON we.id = s.workout_exercise_id
  JOIN public.workouts w ON we.workout_id = w.id
  JOIN public.programs p ON w.program_id = p.id
  WHERE p.user_id = p_user
  GROUP BY e.id, e.name
  ORDER BY MAX(s.performed_at) DESC
  LIMIT 50;
$$;

CREATE OR REPLACE FUNCTION public.get_user_progress(p_user_id uuid DEFAULT auth.uid())
RETURNS TABLE(user_id uuid, exercise text, week timestamptz, avg_weight numeric, avg_reps numeric)
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public, pg_temp AS $$
  SELECT p.user_id, e.name, date_trunc('week', s.performed_at), AVG(s.weight), AVG(s.reps)
  FROM public.sets s
  JOIN public.workout_exercises we ON s.workout_exercise_id = we.id
  JOIN public.workouts w ON we.workout_id = w.id
  JOIN public.programs p ON w.program_id = p.id
  JOIN public.exercises e ON we.exercise_id = e.id
  WHERE p.user_id = p_user_id
  GROUP BY p.user_id, e.name, date_trunc('week', s.performed_at)
  ORDER BY date_trunc('week', s.performed_at) DESC;
$$;

CREATE OR REPLACE FUNCTION public.get_exercise_history(p_user_id uuid, p_exercise_id integer, p_limit integer DEFAULT 10)
RETURNS TABLE(weight numeric, reps integer, rir integer, performed_at timestamptz, max_weight numeric)
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public, pg_temp AS $$
  SELECT s.weight, s.reps, s.rir, s.performed_at,
         MAX(s.weight) OVER (PARTITION BY we.exercise_id) AS max_weight
  FROM public.sets s
  JOIN public.workout_exercises we ON s.workout_exercise_id = we.id
  JOIN public.workouts w ON we.workout_id = w.id
  JOIN public.programs p ON w.program_id = p.id
  WHERE p.user_id = p_user_id AND we.exercise_id = p_exercise_id
  ORDER BY s.performed_at DESC
  LIMIT p_limit;
$$;

CREATE OR REPLACE FUNCTION public.get_exercise_1rm_data(p_user_id uuid, p_exercise_id integer)
RETURNS TABLE(weight numeric, reps integer, rir integer, performed_at timestamptz, estimated_1rm numeric)
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public, pg_temp AS $$
  SELECT s.weight, s.reps, s.rir, s.performed_at,
         CASE WHEN s.reps > 0 AND s.weight > 0
              THEN s.weight * (1::numeric + ((s.reps + COALESCE(s.rir,0))::numeric / 30.0))
              ELSE NULL END
  FROM public.sets s
  JOIN public.workout_exercises we ON s.workout_exercise_id = we.id
  JOIN public.workouts w ON we.workout_id = w.id
  JOIN public.programs p ON w.program_id = p.id
  WHERE p.user_id = p_user_id AND we.exercise_id = p_exercise_id
  ORDER BY s.performed_at DESC;
$$;

CREATE OR REPLACE FUNCTION public.get_exercise_stats(p_user_id uuid, p_exercise_id integer)
RETURNS TABLE(exercise_id integer, exercise_name text, manual_1rm numeric, manual_1rm_updated_at timestamptz,
              calculated_1rm numeric, best_1rm numeric, total_sets bigint, avg_weight numeric, avg_reps numeric,
              last_performed timestamptz, pump_score integer, notes text)
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public, pg_temp AS $$
  WITH set_data AS (
    SELECT s.weight, s.reps, s.rir, s.performed_at,
      CASE WHEN s.reps > 0 AND s.weight > 0
           THEN s.weight * (1::numeric + ((s.reps + COALESCE(s.rir,0))::numeric / 30.0))
           ELSE NULL END AS est_1rm
    FROM public.sets s
    JOIN public.workout_exercises we ON s.workout_exercise_id = we.id
    JOIN public.workouts w ON we.workout_id = w.id
    JOIN public.programs p ON w.program_id = p.id
    WHERE p.user_id = p_user_id AND we.exercise_id = p_exercise_id
  )
  SELECT e.id, e.name, r.manual_1rm, r.manual_1rm_updated_at,
         (SELECT est_1rm FROM set_data ORDER BY performed_at DESC LIMIT 1),
         (SELECT MAX(est_1rm) FROM set_data),
         (SELECT COUNT(*) FROM set_data),
         (SELECT AVG(weight) FROM set_data),
         (SELECT AVG(reps) FROM set_data),
         (SELECT MAX(performed_at) FROM set_data),
         r.pump_score, r.notes
  FROM public.exercises e
  LEFT JOIN public.ratings r ON r.exercise_id = e.id AND r.user_id = p_user_id
  WHERE e.id = p_exercise_id;
$$;

CREATE OR REPLACE FUNCTION public.get_core_lift_progression(p_user_id uuid DEFAULT auth.uid())
RETURNS TABLE(core_lift_type text, exercise_name text, workout_date date, best_estimated_1rm numeric,
              total_volume numeric, avg_weight numeric, total_sets bigint)
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public, pg_temp AS $$
  SELECT cl.core_lift_type, e.name, w.workout_date,
         MAX(CASE WHEN s.reps > 0 AND s.weight > 0
                  THEN s.weight * (1::numeric + ((s.reps + COALESCE(s.rir,0))::numeric / 30.0)) END),
         SUM(s.weight * s.reps),
         AVG(s.weight),
         COUNT(s.id)
  FROM public.sets s
  JOIN public.workout_exercises we ON s.workout_exercise_id = we.id
  JOIN public.workouts w ON we.workout_id = w.id
  JOIN public.programs p ON w.program_id = p.id
  JOIN public.exercises e ON we.exercise_id = e.id
  JOIN public.v_core_lifts cl ON cl.id = e.id
  WHERE p.user_id = p_user_id AND cl.core_lift_type IS NOT NULL AND w.completed = true
  GROUP BY cl.core_lift_type, e.name, w.workout_date
  ORDER BY w.workout_date DESC;
$$;

CREATE OR REPLACE FUNCTION public.get_current_core_lift_maxes(p_user_id uuid DEFAULT auth.uid())
RETURNS TABLE(core_lift_type text, exercise_name text, current_1rm numeric, last_performed date, improvement_30d numeric)
LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public, pg_temp AS $$
  WITH prog AS (
    SELECT * FROM public.get_core_lift_progression(p_user_id)
  )
  SELECT core_lift_type,
         (SELECT exercise_name FROM prog p2 WHERE p2.core_lift_type = p.core_lift_type ORDER BY workout_date DESC LIMIT 1),
         MAX(best_estimated_1rm) FILTER (WHERE workout_date = (SELECT MAX(workout_date) FROM prog p3 WHERE p3.core_lift_type = p.core_lift_type)),
         MAX(workout_date),
         MAX(best_estimated_1rm) FILTER (WHERE workout_date >= (CURRENT_DATE - INTERVAL '30 days'))
         - MAX(best_estimated_1rm) FILTER (WHERE workout_date < (CURRENT_DATE - INTERVAL '30 days'))
  FROM prog p
  GROUP BY core_lift_type;
$$;

CREATE OR REPLACE FUNCTION public.get_workout_frequency_stats(p_user_id uuid DEFAULT auth.uid())
RETURNS TABLE(total_workouts bigint, avg_workouts_per_week numeric, current_streak integer, longest_streak integer, last_workout_date date)
LANGUAGE plpgsql STABLE SECURITY DEFINER SET search_path = public, pg_temp AS $$
DECLARE
  v_total bigint;
  v_last date;
  v_first date;
  v_weeks numeric;
BEGIN
  SELECT COUNT(*), MAX(w.workout_date), MIN(w.workout_date)
    INTO v_total, v_last, v_first
  FROM public.workouts w
  JOIN public.programs p ON w.program_id = p.id
  WHERE p.user_id = p_user_id AND w.completed = true;

  v_weeks := GREATEST(1, EXTRACT(EPOCH FROM (COALESCE(v_last, CURRENT_DATE) - COALESCE(v_first, CURRENT_DATE)))/604800.0);
  RETURN QUERY SELECT v_total, ROUND(v_total::numeric / v_weeks, 2), 0, 0, v_last;
END;
$$;

